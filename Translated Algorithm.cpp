#include <iostream>

using namespace std;

int main () {
	// Initializing Variables
	int x = 4, y=2;
	
	// Initializing Formulas
	int sum = x + y; 
	int difference = x - y; 
	int product = x * y; 
	int quotient = x / y;
	int remainder = x % y;
	
	// Output of Procedure
	cout << "Sum: " << sum << endl;
	cout << "Difference: " << difference << endl;
	cout << "Product: " << product << endl;
	cout << "Quotient: " << quotient << endl;
	cout << "Remainder: " << remainder << endl;
	
	return 0;
}
